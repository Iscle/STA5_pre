static int DABRadio(void)
{
    tU8 DABMWcmdPing[3] = {0x90, 0x00, 0x00};
    static tU8 data[32768];
    tU32 len;
    tU8 lun;
    tBool got_data = 0;

    STECIDeviceConfiguration.deviceHandle = 0;
    STECIDeviceConfiguration.deviceSecondaryHandle = 0;
    STECIDeviceConfiguration.deviceMode = 0;

    printf("Send message to ping DAB DCOP\n");

    if (DAB_Send_Message((tU8)0x30, DABMWcmdPing,
         STECI_NORMAL_CMD, STECI_BOOT_MODE_NORMAL, STECI_TARGET_SPI2FLASH,
         STECI_ACCESS_DATA_MODE, (tU16)sizeof(DABMWcmdPing)) != OSAL_OK)
    {
        /* error condition, message not sent: don't wait for a response */
        return 0;
    }
    else
    {
        usleep(1000000);

        do
        {
            got_data = 0;
            if (DAB_Get_Response(data, 32768, &len, &lun) == OSAL_OK)
            {
                got_data = (len != 0) ? 1 : 0;
            }
        } while (got_data);
    }

    DAB_Reset();

    /* Time to load the FW from flash and execute it */
    usleep(DAB_FW_LOADING_TIME * 1000);


    printf("Send message to ping DAB DCOP\n");

    if (DAB_Send_Message((tU8)0x30, DABMWcmdPing,
         STECI_NORMAL_CMD, STECI_BOOT_MODE_NORMAL, STECI_TARGET_SPI2FLASH,
         STECI_ACCESS_DATA_MODE, (tU16)sizeof(DABMWcmdPing)) != OSAL_OK)
    {
        /* error condition, message not sent: don't wait for a response */
        return 0;
    }
    else
    {
        usleep(1000000);

        do
        {
            got_data = 0;
            if (DAB_Get_Response(data, 32768, &len, &lun) == OSAL_OK)
            {
                got_data = (len != 0) ? 1 : 0;
            }
        } while (got_data);
    }


    return 0;
}
