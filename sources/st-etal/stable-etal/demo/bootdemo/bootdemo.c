/***************************
 *
 * main
 *
 **************************/
/*
 * Returns:
 * 1 error(s)
 * 0 success
 */
int main(int argc, char **argv)
{
#ifdef BOOT_TEST_MODE_CMOST
	if (TUNERDRIVER_testRead_CMOST(CMOST_TUNER_ID_0) != OSAL_OK)
	{
		return 1;
	}
#endif

	if (parseParameters(argc, argv))
	{
		return 1;
	}

#if defined (BOOT_MEASURE_TIME)
	OSAL_ClockResetTime();
#endif

    if (TUNERDRIVER_system_init() != OSAL_OK)
    {
        printf("Error TUNERDRIVER_system_init\n");
        return 1;
    }

	if (initCMOST())
	{
		printf("Error initializing the CMOST\n");
		return 1;
	}

#if defined (HAVE_HD)
	if(initDCOP_HDRADIO())
	{
		printf("Error initializing the DCOP HDRADIO\n");
		return 1;
	}
#endif

#if defined (HAVE_DAB)
    if(initDCOP_DAB())
    {
        printf("Error initializing the DCOP DAB\n");
        return 1;
    }
#endif

	if (Radio() != 0)
	{
		printf("Error during CMOST activity \n");
		return 1;
	}
	else
	{
#if defined (BOOT_MEASURE_TIME)
		printf("Time from reset to tune end: %dmsec\n", OSAL_ClockGetElapsedTime());
#endif
	}

#if defined (HAVE_HD)
	if (HDRadio() != 0)
	{
		printf("Error during DCOP HD Radio activity \n");
		return 1;
	}

	/* Issue a hardware RESET to the DCOP using the HDRADIO library function */
	if (HDRADIO_reset() != OSAL_OK)
	{
		printf("Error in HDRADIO_reset\n");
		return 1;
	}

	/* Wait some time to load the FW from flash and execute it */
    usleep(HDRADIO_FW_LOADING_TIME*1000);

	if (HDRadio() != 0)
	{
		printf("Error during DCOP HD Radio activity \n");
		return 1;
	}

	/* Issue a hardware RESET to the DCOP using the HDRADIO library function */
	if (HDRADIO_deinit() != OSAL_OK)
	{
		printf("Error in HDRADIO_reset\n");
		return 1;
	}
#endif

#if defined (HAVE_DAB)
    if (DABRadio() != 0)
    {
        printf("Error during DCOP DAB Radio activity \n");
        return 1;
    }
#endif

    if (TUNERDRIVER_system_deinit() != OSAL_OK)
    {
        printf("Error TUNERDRIVER_system_deinit\n");
        return 1;
    }

	return 0;
}

