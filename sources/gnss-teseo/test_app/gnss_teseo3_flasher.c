  int id = 1,bufsize =0;
  long int value;
  FILE * file_handle =NULL;
  int xloader_flag = -1;
  char * ptr_bn = NULL;
  int Gpio_user = 0, Host_uart_user = 0;

  printf("\r\n");
  printf("Started Teseo3 flasher version %s\r\n", flasher_version);
  printf("\r\n");
  get_appli_name(argv[0]);

  if(argc ==1) {
    teseo_flasher_params_usage();
    goto end;
  }
  /* #######################  Check application parameters  ####################### */
  while (id < argc)
  {
    if (strcmp("-f" , argv[id]) == 0) {
      id++;
      if(id<argc) {
        bufsize = strlen(argv[id]) + 1;
        strncpy(filename,argv[id],bufsize);
        file_handle = fopen(filename, "rb");
        if(file_handle == NULL ) {
           printf("Invalid file\n");
           goto end;
        }
        fclose(file_handle);
        id++;
      }
      else {
        printf("Enter valid file name\n");
        goto end;
      }
    }
    else if (strcmp("-b" , argv[id]) == 0) {
      id++;
      if(id<argc)
      {
        change_baudrate = atoi(argv[id]);
        for (i = 0; uart_baud_rates[i].rate; i++)
        {
          if (uart_baud_rates[i].rate <= change_baudrate) {
            change_baudrate = uart_baud_rates[i].rate;
            baudrate_index =  uart_baud_rates[i].index;
            break;
          }
        }
        if(change_baudrate > 921600 || change_baudrate < 57600) {
          change_baudrate = 0;
          printf("BaudRate entered is not in valid limit, default <115200> will be used\n");
        }
        id++;
      }
      else {
        printf("Enter baud rate\n");
        goto end;
      }

    }
    else if(strcmp("-r" , argv[id]) == 0) {
       recovery =1;
       id++;
    }
    else if(strcmp("-x" , argv[id]) == 0) {
       xloader_flag = 1;
       id++;
    }
    else if(strcmp("-u" , argv[id]) == 0) {
       xloader_flag = 0;
       id++;
    }
    else if(strcmp("-n" , argv[id]) == 0) {
       nvm_erase =1;
       id++;
    }
    else if(strcmp("-e" , argv[id]) == 0) {
       erase_only =1;
       id++;
    }
    else if(strcmp("-p" , argv[id]) == 0) {
       program_only =1;
       id++;
    }
    else if (strcmp("IN", argv[id]) == 0) {
       id++;
       errno = 0;
       value = strtol(argv[id], (char **)NULL, 10);
       if ((errno != EINVAL) && (errno != ERANGE)) {
         Host_uart_port = (int)value;
         Host_uart_user = 1;
       }
       printf("Teseo flasher: Host<->Teseo UART set to port %d\r\n", Host_uart_port);
       id++;
    }
    else if (strcmp("GPIO", argv[id]) == 0) {
       id++;
       errno = 0;
       value = strtol(argv[id], (char **)NULL, 10);
       if ((errno != EINVAL) && (errno != ERANGE)) {
         GpioTeseoReset = (int)value;
         Gpio_user = 1;
       }
       printf("Teseo flasher: Teseo reset GPIO set to %d\r\n", GpioTeseoReset);
       id++;
    }
    else if (strcmp("-no_reset" , argv[id]) == 0) {
       TeseoReset_flag = 0;
       printf("Teseo flasher: Teseo reset canceled\r\n");
       id++;
    }
    else {
       teseo_flasher_params_usage();
       goto end;
    }
  }
  
  /* #######################  Set the Board parameters  ####################### */
  ret = get_board_model(model);
  if (ret) {
    printf("Failed to retrieve board model!!\r\n");
    goto end;
  }
  else {
    printf("Detected board model : \"%s\"\r\n\n", model);
  }

  for (id = 0; id<BOARD_NUMBERS ; id++)
  {
    if (strstr(model, board_params[id].model)) {
       if (Gpio_user == 0)
         GpioTeseoReset = board_params[id].gpio_reset;
       if (Host_uart_user == 0)
         Host_uart_port = board_params[id].host_uart_port;
    }
  }
  sprintf(Uart2Host ,"/dev/ttyAMA%d", Host_uart_port);

  /* ########  Check if another process is using the serial port  ############# */
  sprintf(uartchk, "ls -l /proc/[0-9]*/fd/* 2> /dev/null | grep -c %s > /dev/null 2>&1", Uart2Host);
  if (system(uartchk) == 0) {
    sprintf(uartchk, "ls -l /proc/[0-9]*/fd/* 2> /dev/null | grep %s 2> /dev/null", Uart2Host);
    printf("Unable to proceed: Output UART port %s is already used by process\r\n", Uart2Host);
    system(uartchk);
    goto end;
  }

  if(xloader_flag == 1)
  {
    if((erase_only == 1) && (program_only == 1) )
    {
      printf("input -p <program only> and -e <erase only flash> are exclusive.Please select only one at a time : -h for help \r\n");
      goto end;
    }
    ptr_bn = strstr(filename,"BOOT26" );
    if (ptr_bn == NULL) {
      printf("WARNING: TESEO FW FLASHER BINARY NAME FMT should be STAxxxx_x_x_x_x_BOOT26.bin\n");
      printf("         Make sure input binary is correct\n\n");
    }
    /* #####################  Perform Teseo Upgrade  ############################ */
    printf("\n");
    printf("*********************************************\n");
    printf("STARTING TESEO III FW FLASHING USING XLOADER \n");
    printf("DO NOT POWER-OFF THE BOARD UNTIL IT'S DONE!!!\n");
    printf("*********************************************\n");
    ret = sal_fwxloader_exe();
  }    
