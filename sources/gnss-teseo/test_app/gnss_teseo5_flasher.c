int open_uart_port(char *device)
{
  int uartfd;
  struct termios newtio;

  uartfd = open(device, O_RDWR | O_NOCTTY);

  if (uartfd == -1) {
    printf("Unable to open UART %s, error %d\r\n", device, errno);
    return errno;
  }

  tcgetattr(uartfd, &newtio);

  newtio.c_cflag = B115200;
  newtio.c_cflag |= CLOCAL | CREAD;
  newtio.c_cflag |= CS8;
  newtio.c_iflag &= ~(IXON|ICRNL|INLCR);
  newtio.c_oflag = 0;
  newtio.c_lflag = 0;
  newtio.c_cc[VMIN] = 1;
  newtio.c_cc[VTIME] = 0;

  if (tcflush(uartfd, TCIOFLUSH)) {
    printf("ERROR: tcflush\r\n");
    stop_application();
  }

  if (tcsetattr(uartfd,TCSANOW,&newtio)) {
    printf("ERROR: tcsetattr\r\n");
    stop_application();
  }

  return uartfd;
}
